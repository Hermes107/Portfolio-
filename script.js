var modal = new bootstrap.Modal(document.getElementById('projectModal'));

function createLightbox() {
    var lightboxHTML = '<div id="lightbox" style="display:none; position:fixed; z-index:9999; left:0; top:0; width:100%; height:100%; background-color:rgba(0,0,0,0.9); cursor:zoom-out;">';
    lightboxHTML += '<span style="position:absolute; top:20px; right:40px; color:#fff; font-size:40px; font-weight:bold; cursor:pointer;">&times;</span>';
    lightboxHTML += '<img id="lightbox-img" style="position:absolute; top:50%; left:50%; transform:translate(-50%, -50%); max-width:90%; max-height:90%; object-fit:contain;">';
    lightboxHTML += '</div>';
    
    document.body.insertAdjacentHTML('beforeend', lightboxHTML);
    
    var lightbox = document.getElementById('lightbox');
    var lightboxImg = document.getElementById('lightbox-img');
    
    lightbox.onclick = function() {
        lightbox.style.display = 'none';
    };
}

// Initialize lightbox on page load
document.addEventListener('DOMContentLoaded', function() {
    createLightbox();
});

function openModal(index) {
    var project = projects[index];
    
    document.getElementById('modal-academic').textContent = project.academic;
    document.getElementById('modal-academic').style.display = project.academic ? 'block' : 'none';
    document.getElementById('modal-category').textContent = project.category;
    document.getElementById('modal-title').textContent = project.title;
    
    var bodyHTML = '';
    
    // this will add a live website link 
    if (project.liveLink) {
        bodyHTML += '<div class="mb-4 text-center">';
        bodyHTML += '<a href="' + project.liveLink + '" target="_blank" rel="noopener noreferrer" class="btn btn-lg btn-primary live-link-btn">';
        bodyHTML += '<i class="bi bi-box-arrow-up-right me-2"></i>Visit Live Website';
        bodyHTML += '</a>';
        bodyHTML += '</div>';
    }
    var hasButtons = project.liveLink || project.figmaLink || project.pdfReport || project.slidesLink;
    
    if (hasButtons) {
        bodyHTML += '<div class="mb-4">';
        bodyHTML += '<div class="d-flex flex-wrap gap-3 justify-content-center">';
    }
       
        // Figma Prototype Link
        if (project.figmaLink) {
            bodyHTML += '<a href="' + project.figmaLink + '" target="_blank" rel="noopener noreferrer" class="btn btn-figma action-btn">';
            bodyHTML += '<i class="bi bi-palette me-2"></i>View Figma Prototype';
            bodyHTML += '</a>';
        }
        
        // PDF Report Download
        if (project.pdfReport) {
            bodyHTML += '<a href="' + project.pdfReport + '" target="_blank" class="btn btn-report action-btn">';
            bodyHTML += '<i class="bi bi-file-earmark-pdf me-2"></i>Download Report (PDF)';
            bodyHTML += '</a>';
        }
    
    // Add images when they 
    if (project.images && project.images.length > 0) {
        bodyHTML += '<div class="mb-4">';
        bodyHTML += '<div class="row g-3">';
        for (var imgIndex = 0; imgIndex < project.images.length; imgIndex++) {
            var colClass = project.images.length === 1 ? 'col-12' : 'col-md-6';
            bodyHTML += '<div class="' + colClass + '">';
            bodyHTML += '<img src="' + project.images[imgIndex] + '" class="img-fluid rounded shadow" alt="' + project.title + ' - Image ' + (imgIndex + 1) + '" onclick="openLightbox(this.src)">';
            bodyHTML += '</div>';
        }
        bodyHTML += '</div></div>';
    }
    
    bodyHTML += '<div class="mb-4"><h5 class="fw-bold">Challenge</h5><p class="text-secondary">' + project.challenge + '</p></div>';
    bodyHTML += '<div class="mb-4"><h5 class="fw-bold">Solution</h5><p class="text-secondary" style="white-space: pre-line;">' + project.solution + '</p></div>';
    
    if (project.process) {
        bodyHTML += '<div class="mb-4"><h5 class="fw-bold">Design Process</h5><p class="text-secondary" style="white-space: pre-line;">' + project.process + '</p></div>';
    }
    
    bodyHTML += '<div class="mb-4"><h5 class="fw-bold">Impact</h5><p class="text-secondary">' + project.impact + '</p></div>';
    bodyHTML += '<div class="border-top pt-3">';
    
    for (var tagIndex = 0; tagIndex < project.tags.length; tagIndex++) {
        bodyHTML += '<span class="modal-tag">' + project.tags[tagIndex] + '</span>';
    }
    
    bodyHTML += '</div>';
    
    document.getElementById('modal-body').innerHTML = bodyHTML;
    modal.show();
}
function openLightbox(src) {
    var lightbox = document.getElementById('lightbox');
    var lightboxImg = document.getElementById('lightbox-img');
    lightboxImg.src = src;
    lightbox.style.display = 'block';
}

// Smooth scrolling
document.querySelectorAll('a[href^="#"]').forEach(function(anchor) {
    anchor.addEventListener('click', function(e) {
        e.preventDefault();
        var target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({ behavior: 'smooth' });
            var navCollapse = document.querySelector('.navbar-collapse');
            if (navCollapse.classList.contains('show')) {
                bootstrap.Collapse.getInstance(navCollapse).hide();
            }
        }
    });
});




